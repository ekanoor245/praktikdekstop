    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String kodeBrg,namaBrg,qty,harga, total;
        kodeBrg = txtKodebrg.getText();
        namaBrg = txtNamabrg.getText();
        qty     = txtQty.getText();
        harga   = txtHarga.getText();
        total   = Integer.toString(Integer.parseInt(harga)*Integer.parseInt(qty));
        // masukkan data ke object string
        String data[]={kodeBrg, namaBrg, harga, qty, total};
        // tambahkan ke grid nota
        model.addRow(data);
        //kosongkan isian
        txtKodebrg.setText("");
        txtNamabrg.setText("");
        
        txtQty.setText("");
        txtHarga.setText("");
        
        //fokus letakkan di kodebrg
        txtKodebrg.requestFocus();
        //hitung ulang total
        hitungTotal();
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int nPilih = tblNota.getSelectedRow();
        if (nPilih != -1) {
            model.removeRow(nPilih);
            hitungTotal();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

