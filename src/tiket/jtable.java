   private void kosongkan() {
        txtKodebrg.setText("");
        txtNamabrg.setText("");
        txtHarga.setText("");
        txtQty.setText("");
        txtTotal.setText("0");
        txtDisplay.setText("0");
        //No nota tambah 1
        int nomor = Integer.parseInt(txtNonota.getText())+1;
        txtNonota.setText(Integer.toString(nomor));
        //kosongkan grid Nota
        while (tblNota.getRowCount() > 0) {
            model.removeRow(0);
        }
        //fokus letakkan di kodebrg
        txtKodebrg.requestFocus();
    }
     private void hitungTotal(){
        int jumBaris = tblNota.getRowCount();
        int tot = 0;
        for (int i=0; i<jumBaris; i++){
            // ambil data total dari grid (kolom ke 4)
            int nJum = Integer.parseInt((String) tblNota.getValueAt(i,4));
            tot += nJum;
        }
        txtTotal.setText(Integer.toString(tot));
        //tampilkan di display dengan pemisah ribuan
        DecimalFormat ribuan = new DecimalFormat("###,###");
        txtDisplay.setText(ribuan.format(tot));
}
